print(type([1, 2, 3])) #Tipo 'lista'

print(2 + 5) #Adicion
print(2 - 5) #Sustraccion
print(2 * 5) #Producto o multiplicacion
print(2 / 5) #Division
print(2 ** 5) #Exponencial
print (2 % 5) #Modulo
print(2 // 5) #Floor division operator


